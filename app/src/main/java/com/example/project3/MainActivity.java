        a = new Vinyl("Drake", "Views", 5, covers[1]);
        vinylList.add(a);

        a = new Vinyl("Drake", "Emotionless", 5, covers[2]);
        vinylList.add(a);

        a = new Vinyl("Jay-Z", "Friend or foe", 5, covers[3]);
        vinylList.add(a);

